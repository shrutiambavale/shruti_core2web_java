
class If{
	public static void main(String[] args){
		int n=5;
	       System.out.println("Before Switch");
       		switch(n){
	 		case 1:
			       System.out.println("1");
	
			case 2:
	       			System.out.println("2");
		
			case 3:
	       			System.out.println("3");
			default:
				System.out.println("Default");
		}
	       			System.out.println("After Switch");


	}
}


