class Switch7{
	public static void main(String[] args){
		int num=2000;
		switch(num){
			case 15000:
				System.out.println("Jammu and Kasmir");
				break;
			case 10000:
				System.out.println("Manali");
				break;
			case 6000:
				System.out.println("Amritsar");
				break;
			case 2000:
				System.out.println("Mahabaleshwar");
				break;
			default:
				System.out.println("Try next time");
				break;
		}
	}
}
		
